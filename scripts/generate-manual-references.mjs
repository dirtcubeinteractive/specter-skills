#!/usr/bin/env node
/**
 * Distributes the Specter USER MANUAL (conceptual / dashboard docs) into each
 * skill's references, so the skills carry the product knowledge — what a
 * container is, how the task rule engine works, currency/item/bundle types,
 * skill divisions, leaderboard ranking methods, brackets, campaigns/seasons —
 * not just the raw API field shapes.
 *
 * Source of truth: specs/manual/*.md   (vendored from manual.specterapp.xyz,
 *                  one file per page, slug = path with '/' → '__')
 * Output:          skills/<skill>/references/manual/<slug>.md  (cleaned)
 *                  skills/<skill>/references/manual-index.md   (generated)
 *
 * Refresh the vendored manual with scripts/fetch-manual.sh, then re-run this.
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, rmSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const SRC = join(ROOT, 'specs', 'manual');
const SKILLS = join(ROOT, 'skills');

// Route a page slug (path with '/' → '__') to the skill whose domain it documents.
function skillFor(slug) {
  const rules = [
    [/^unity-sdk__/, 'specter'],
    [/^getting-started__/, 'specter'],
    [/^organisation__/, 'specter-admin'],
    [/^app__/, 'specter-admin'],
    [/^build__economy/, 'specter-economy'],
    [/^build__progression/, 'specter-progression'],
    [/^build__events/, 'specter-progression'],
    [/^build__matches/, 'specter-multiplayer'],
    [/^build__games/, 'specter'],
    [/^build__overview/, 'specter'],
    [/^engage__players/, 'specter-players'],
    [/^engage__achievements/, 'specter-progression'],
    [/^engage__leaderboards/, 'specter-competitions'],
    [/^engage__competitions/, 'specter-competitions'],
    [/^engage__live-ops/, 'specter-competitions'],
    [/^engage__overview/, 'specter-players'],
    [/^analyze/, 'specter'],
  ];
  for (const [re, skill] of rules) if (re.test(slug)) return skill;
  return 'specter'; // additional-resources, product-roadmap, community-and-growth, fallbacks
}

// Strip GitBook boilerplate: the leading llms.txt index line and the trailing
// "# Agent Instructions" footer (and its preceding `---`).
function clean(md) {
  let s = md.replace(/^> For the complete documentation index[^\n]*\n/, '');
  const footer = s.indexOf('\n# Agent Instructions');
  if (footer !== -1) {
    // also drop a `---` separator immediately before the footer
    let cut = footer;
    const before = s.slice(0, footer).replace(/\n+\s*---\s*$/, '');
    s = before;
  }
  return s.trim() + '\n';
}

function titleOf(md) {
  const m = md.match(/^#\s+(.+)$/m);
  return m ? m[1].trim() : null;
}

const files = readdirSync(SRC).filter((f) => f.endsWith('.md'));
if (!files.length) {
  console.error(`✗ no manual pages in ${SRC} — run scripts/fetch-manual.sh first`);
  process.exit(1);
}

// group by target skill
const bySkill = {};
for (const file of files) {
  const slug = file.replace(/\.md$/, '');
  const skill = skillFor(slug);
  const cleaned = clean(readFileSync(join(SRC, file), 'utf8'));
  const title = titleOf(cleaned) || slug;
  (bySkill[skill] ||= []).push({ slug, file, title, cleaned, section: slug.split('__')[0] });
}

let total = 0;
for (const [skill, pages] of Object.entries(bySkill)) {
  const outDir = join(SKILLS, skill, 'references', 'manual');
  if (!existsSync(join(SKILLS, skill))) {
    console.error(`✗ skill dir missing: ${skill} — skipping ${pages.length} pages`);
    continue;
  }
  rmSync(outDir, { recursive: true, force: true });
  mkdirSync(outDir, { recursive: true });
  for (const p of pages) {
    writeFileSync(join(outDir, `${p.slug}.md`), p.cleaned);
    total++;
  }
  // per-skill index grouped by section
  const bySection = {};
  for (const p of pages) (bySection[p.section] ||= []).push(p);
  const idx = [
    `# ${skill} — user-manual concept index`,
    '',
    '> Generated by `scripts/generate-manual-references.mjs` from the Specter user manual.',
    '> These pages explain the CONCEPTS and dashboard workflows behind this domain — read them',
    '> to understand intent and terminology; pair them with the API/endpoint references for the',
    '> exact request shapes.',
    '',
  ];
  for (const section of Object.keys(bySection).sort()) {
    idx.push(`## ${section}`, '');
    for (const p of bySection[section].sort((a, b) => a.slug.localeCompare(b.slug))) {
      idx.push(`- [${p.title}](manual/${p.slug}.md)`);
    }
    idx.push('');
  }
  writeFileSync(join(SKILLS, skill, 'references', 'manual-index.md'), idx.join('\n'));
  console.log(`✓ ${skill.padEnd(20)} ${pages.length} manual pages`);
}
console.log(`\n✓ distributed ${total} manual pages across ${Object.keys(bySkill).length} skills`);
