#!/usr/bin/env node
/**
 * Generates per-skill endpoint references from the backend's per-endpoint docs.
 *
 * Source of truth: gamestarz_dashboard_backend/docs/api/*.md
 * Output:          skills/<skill>/references/endpoints/*.md  (verbatim copies)
 *                  skills/<skill>/references/endpoints-index.md (generated index)
 *
 * Every source file MUST match a mapping rule — unmapped files fail the build,
 * so new backend endpoints can't silently go undocumented in the skills.
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, rmSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const DOCS_DIR =
  process.env.SPECTER_DOCS_DIR ??
  join(ROOT, '..', 'gamestarz_dashboard_backend', 'docs', 'api');
const SKILLS_DIR = join(ROOT, 'skills');

// First matching rule wins.
const RULES = [
  [/^(auth|account|friends)-/, 'specter-players'],
  [/^app-get-players/, 'specter-players'],
  [/^player(-me)?-(get-profile|update-data|get-data|get-single-data|remove-data|upload-icon|get-friends|get-pending-friend-requests|get-sent-friend-requests)/, 'specter-players'],

  [/^(wallet|rmg|stores|inventory)-/, 'specter-economy'],
  [/^get-currencies/, 'specter-economy'],
  [/^app-get-(items|bundles|gacha-bundle-config|store-categories|store-category-contents|stores|goc-random-bots)/, 'specter-economy'],
  [/^player(-me)?-get-(rm-wallet-history|wallet-history|inventory|collections)/, 'specter-economy'],

  [/^(achievements|battlepass|progression)-/, 'specter-progression'],
  [/^get-(tasks|missions|step-series|time-series)/, 'specter-progression'],
  [/^app-get-(battlepasses|progression-systems|markers)/, 'specter-progression'],
  [/^player(-me)?-get-(task-status|task-progress|task-group-status|time-series-progress|reward-history|progress)/, 'specter-progression'],
  [/^events-send-custom/, 'specter-progression'],

  [/^(leaderboards|competitions?)-/, 'specter-competitions'],
  [/^(app-get-)?ugc-leaderboards-/, 'specter-competitions'],
  [/^app-get-ugc-leaderboards/, 'specter-competitions'],
  [/^get-(leaderboards|tournaments|instant-battles)/, 'specter-competitions'],
  [/^app-get-(tournaments|instant-battles)/, 'specter-competitions'],
  [/^player(-me)?-get-(tournament-history|instant-battle-history)/, 'specter-competitions'],
  [/^liveops-get-(competition|leaderboard)-schedule-history/, 'specter-competitions'],

  [/^(matchmaking|matches|party)-/, 'specter-multiplayer'],
  [/^(app-)?get-matches/, 'specter-multiplayer'],
  [/^player(-me)?-get-match-history/, 'specter-multiplayer'],
  [/^player-me-get-party-details/, 'specter-multiplayer'],

  [/^(app-welcome|get-info|app-get-master-data|get-games|liveops-get-server-time|app-get-document)/, 'specter'],
];

function skillFor(file) {
  for (const [re, skill] of RULES) if (re.test(file)) return skill;
  return null;
}

// Parse title, endpoint and auth from a doc file for the index.
function parseDoc(content) {
  const title = content.match(/^#\s+(.+)$/m)?.[1]?.trim() ?? '';
  const endpoint = content.match(/\*\*Endpoint:\*\*\s*`?([^`\n]+)`?/)?.[1]?.trim() ?? '';
  const auth = content.match(/\*\*Authentication:\*\*\s*([^\n]+)/)?.[1]?.trim() ?? '';
  return { title, endpoint, auth };
}

const files = readdirSync(DOCS_DIR).filter((f) => f.endsWith('.md'));
const unmapped = files.filter((f) => !skillFor(f));
if (unmapped.length) {
  console.error(`✗ ${unmapped.length} endpoint doc(s) not mapped to any skill:`);
  for (const f of unmapped) console.error(`  - ${f}`);
  process.exit(1);
}

const bySkill = new Map();
for (const f of files) {
  const s = skillFor(f);
  if (!bySkill.has(s)) bySkill.set(s, []);
  bySkill.get(s).push(f);
}

for (const [skill, skillFiles] of bySkill) {
  const outDir = join(SKILLS_DIR, skill, 'references', 'endpoints');
  rmSync(outDir, { recursive: true, force: true });
  mkdirSync(outDir, { recursive: true });

  const rows = [];
  for (const f of skillFiles.sort()) {
    const content = readFileSync(join(DOCS_DIR, f), 'utf8');
    writeFileSync(join(outDir, f), content);
    const { endpoint, auth } = parseDoc(content);
    rows.push(`| \`${endpoint || f}\` | ${auth || '-'} | [${f}](endpoints/${f}) |`);
  }

  const index = [
    `# ${skill} — endpoint index`,
    '',
    '> Generated by `scripts/generate-references.mjs`. Do not edit by hand.',
    '> Each linked file contains the full request DTO, attributes, and response examples.',
    '',
    '| Endpoint | Auth | Reference |',
    '|---|---|---|',
    ...rows,
    '',
  ].join('\n');
  writeFileSync(join(SKILLS_DIR, skill, 'references', 'endpoints-index.md'), index);
  console.log(`✓ ${skill}: ${skillFiles.length} endpoints`);
}
console.log(`✓ ${files.length} endpoint docs mapped across ${bySkill.size} skills`);
