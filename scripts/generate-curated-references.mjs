#!/usr/bin/env node
/**
 * Generates CURATED client-API references from `api-config-lite.json` — the
 * human-written API config that powers Specter's own documentation assistant.
 * It carries richer descriptions, parameter requiredness, data types and
 * (crucially) `enumValues` for the v2 `attributes` pattern than the raw OpenAPI.
 *
 * Source of truth: specs/client-api-curated.json (vendored from the Specter
 *                  sandbox/assistant backend — `api-config-lite.json`)
 * Output:          skills/<skill>/references/curated-client-api.md  (one per skill, grouped)
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const entries = JSON.parse(readFileSync(join(ROOT, 'specs', 'client-api-curated.json'), 'utf8'));
const SKILLS = join(ROOT, 'skills');

const GROUP_SKILL = {
  App: 'specter',
  Auth: 'specter-players',
  Account: 'specter-players',
  'Player/Me': 'specter-players',
  'Player/Others': 'specter-players',
  Stores: 'specter-economy',
  Inventory: 'specter-economy',
  Wallet: 'specter-economy',
  'Real Money Gaming': 'specter-economy',
  Matches: 'specter-multiplayer',
  Competitions: 'specter-competitions',
  Leaderboards: 'specter-competitions',
  'Live Ops': 'specter-competitions',
  Events: 'specter-progression',
  Progression: 'specter-progression',
  Achievements: 'specter-progression',
};

const esc = (s) => String(s ?? '').replace(/\|/g, '\\|').replace(/\n+/g, ' ').trim();

function paramRows(params) {
  if (!params?.length) return '';
  const rows = params.map((p) => {
    let type = p.dataType || 'any';
    if (p.arrayItemType) type += `<${p.arrayItemType}>`;
    const allowed = p.enumValues?.length ? p.enumValues.map((e) => `\`${e}\``).join(' \\| ') : '';
    return `| \`${p.name}\` | ${type} | ${p.isRequired ? '✅' : '—'} | ${allowed} | ${esc(p.description)} |`;
  });
  return `\n| Param | Type | Required | Allowed values | Description |\n|---|---|---|---|---|\n${rows.join('\n')}\n`;
}

const bySkill = {};
for (const e of entries) {
  const skill = GROUP_SKILL[e.group] || 'specter';
  (bySkill[skill] ||= []).push(e);
}

let total = 0;
for (const [skill, list] of Object.entries(bySkill)) {
  if (!existsSync(join(SKILLS, skill))) continue;
  const byGroup = {};
  for (const e of list) (byGroup[e.group] ||= []).push(e);
  const out = [
    `# ${skill} — curated client (v2) API reference`,
    '',
    '> Generated by `scripts/generate-curated-references.mjs` from Specter\'s own API-assistant',
    '> config. Human-written descriptions + parameter requiredness, types, and `enumValues`',
    '> (especially the `attributes` you can request on v2 client reads). All `POST /v2/client/*`.',
    '',
  ];
  for (const group of Object.keys(byGroup).sort()) {
    out.push(`## ${group}`, '');
    for (const e of byGroup[group].sort((a, b) => String(a.route).localeCompare(String(b.route)))) {
      out.push(`### ${e.name}`, '', `\`${e.method} ${e.route}\``, '', esc(e.description));
      const hdrs = (e.requiredHeaders || []).map((h) => `\`${h.name}\``).join(', ');
      if (hdrs) out.push('', `**Required headers:** ${hdrs}`);
      const pr = paramRows(e.parameters);
      if (pr) out.push(pr);
      out.push('');
      total++;
    }
  }
  writeFileSync(join(SKILLS, skill, 'references', 'curated-client-api.md'), out.join('\n'));
  console.log(`✓ ${skill.padEnd(20)} ${list.length} curated endpoints`);
}
console.log(`\n✓ ${total} curated client endpoints across ${Object.keys(bySkill).length} skills`);
